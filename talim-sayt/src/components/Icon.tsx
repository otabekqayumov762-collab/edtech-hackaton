export function Icon({
  name,
  size = 20,
  className = '',
  strokeWidth = 2,
  color,
  style,
}: {
  name: string;
  size?: number;
  className?: string;
  strokeWidth?: number;
  color?: string;
  style?: React.CSSProperties;
}) {
  const Cmp = MAP[name] ?? Sparkles;
  return (
    <Cmp
      size={size}
      className={className}
      strokeWidth={strokeWidth}
      color={color}
      style={style}
    />
  );
}
